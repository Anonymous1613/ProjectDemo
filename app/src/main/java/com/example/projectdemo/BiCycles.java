package com.example.projectdemo;

public class BiCycles {
    String color;
    int id;

    public BiCycles() {
    }

    public BiCycles(String color, int id) {
        this.color = color;
        this.id = id;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
